DROP ALL OBJECTS;

CREATE TABLE cinema (
	id BIGINT PRIMARY KEY,
	name VARCHAR(100)
);

CREATE TABLE hall (
	id BIGINT PRIMARY KEY,
	name VARCHAR(2),
	num_of_seats BIGINT,
	fk_cinema BIGINT REFERENCES cinema(id)
);

CREATE TABLE seat (
	id BIGINT PRIMARY KEY,
	seatnr BIGINT,
	row BIGINT,
	fk_hall BIGINT REFERENCES hall(id),
);

CREATE TABLE customer(
	id BIGINT PRIMARY KEY,
	firstname VARCHAR(50),
	surname VARCHAR(50),
	email VARCHAR(50),
	dob DATE
);

CREATE TABLE movie (
	id BIGINT PRIMARY KEY,
	name VARCHAR(100),
	running_time BIGINT
);

CREATE TABLE screening (
	id BIGINT PRIMARY KEY,
	starting_time TIMESTAMP,
	fk_movie BIGINT REFERENCES movie(id),
	fk_hall BIGINT REFERENCES hall(id)
);

CREATE TABLE booking (
	id BIGINT PRIMARY KEY AUTO_INCREMENT,
	fk_seat BIGINT REFERENCES seat(id),
	fk_screening BIGINT REFERENCES screening(id)
);



